package com.biostime.wechat.bean;

import java.util.ArrayList;
import java.util.List;

import com.fasterxml.jackson.annotation.JsonInclude;
import com.fasterxml.jackson.annotation.JsonInclude.Include;

@JsonInclude(Include.NON_EMPTY)
public class ButtonItem {
	private String type;
	private String name;
	private String key;
	private String url;
	private String media_id;
	private String appid;
	private String pagepath;
	private List<ButtonItem> sub_button = new ArrayList<ButtonItem>();
	
	public ButtonItem() {
	}

	public ButtonItem(String name) {
		this.name = name;
	}
	
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getKey() {
		return key;
	}
	public void setKey(String key) {
		this.key = key;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getMedia_id() {
		return media_id;
	}
	public void setMedia_id(String media_id) {
		this.media_id = media_id;
	}
	public String getAppid() {
		return appid;
	}
	public void setAppid(String appid) {
		this.appid = appid;
	}
	public String getPagepath() {
		return pagepath;
	}
	public void setPagepath(String pagepath) {
		this.pagepath = pagepath;
	}
	public List<ButtonItem> getSub_button() {
		return sub_button;
	}
	public void setSub_button(List<ButtonItem> sub_button) {
		this.sub_button = sub_button;
	}
}
